export function getDistricts(divisionName: string): District[] {
  const div = divisions.find((d) => d.name === divisionName);
  if (!div) return [];
  return districts.filter((d) => d.divisionId === div.id);
}

export function getUpazilas(districtName: string): Upazila[] {
  const dist = districts.find((d) => d.name === districtName);
  if (!dist) return [];
  return upazilas.filter((u) => u.districtId === dist.id);
}
